package com.example.demo;

public class GameData {
	String p1;
	String p2;
	String content;
	public GameData(String p1, String p2, String content) {
		super();
		this.p1 = p1;
		this.p2 = p2;
		this.content = content;
	}
	public String getNamep2() {
		return p2;
	}
	public String getNamep1() {
		return p1;
	}
}
